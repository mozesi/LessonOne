class MainActivity : AppCompatActivity() {
    private lateinit var drawerLayoutt: DrawerLayout

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        val bindingObj = DataBindingUtil.setContentView<ActivityMainBinding>(this,R.layout.activity_main)
        val navContoroller = this.findNavController(R.id.frag_main)
        drawerLayoutt = bindingObj.drawerLayout

        NavigationUI.setupActionBarWithNavController(this,navContoroller,drawerLayoutt)
       // NavigationUI.setupWithNavController(bindingObj.naView,navContoroller)
    }

     override fun onSupportNavigateUp(): Boolean {
        val navController = this.findNavController(R.id.frag_main)
        return NavigationUI.navigateUp(navController,drawerLayoutt)
    }


























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































